    <h1>Graphql</h1>
    <h2>گراف کیو ال</h2>
    <br>
    <p>
        گراف کیوال (GraphQL) را می‌توان یک query language برای API و یک runtime برای انجام کوئری‌ها بر اساس داده‌های
        موجود دانست که به‌کمک آن می‌توانید فقط داده‌های مورد نیازتان را درخواست کنید و داده‌های اضافی در پاسخ کوئری شما
        ارسال نخواهند شد.
        GraphQL به‌گونه‌ای طراحی شده تا APIها سریع‌تر و انعطاف‌پذیرتر باشند، در نتیجه می‌توان گفت که GraphQL تجربه‌ی
        بهتری از توسعه و کار با API را برای توسعه‌دهندگان فراهم کرده است. همچنین شما به‌عنوان یک توسعه‌دهنده می‌توانید
        داده‌های موردنیازتان را در یک API Call از منابع مختلفی دریافت کنید که این یکی دیگر از قابلیت‌های جالبی است که
        GraphQL به‌عنوان رقیب و جایگزین REST ارائه کرده است.
    </p>
    <br>


