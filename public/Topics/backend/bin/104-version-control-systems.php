    <h1>Version Control Systems</h1>
    <h2>سیستم کنترل ورژن</h2>
    <br>
    <p>
        اگر برنامه نویس هستید و تاکنون با مفهومی به نام سیستم کنترل ورژن یا VCS آشنا نشده اید، بدانید که از یکی از
        مهمترین مفاهیم دنیای برنامه نویسی غافل هستید.
        Version Control System که به اختصار VCS نامیده می‌شوند، سیستمی است که به برنامه نویسان و توسعه دهندگان اجازه
        می‌دهد که بدون اینکه هیچگونه نگرانی داشته باشند، تا دلشان می‌خواهد اشتباه کنند! زیرا آن‌ها خیالشان از این بابت
        راحت است که با استفاده از VCS ها، اشتباهاتشان قابل پیگیری، بررسی و تصحیح هستند. البته این مورد تنها یکی از
        کاربردهای بی نظیر سیستم‌های کنترل نسخه است.
    </p>
    <br>


