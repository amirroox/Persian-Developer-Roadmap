    <h2>Styled JSX</h2>
    <br>
    <p>
        کتابخانه‌های موسوم به CSS-in-Javascript اخیرا به شدت روند رو به رشدی را به خود گرفته‌اند و در حال محبوب شدن هستند. این دسته از کتابخانه‌ها به شما اجازه می‌دهند تا با استفاده از جاوااسکریپت کامپوننت‌های CSS را ایجاد کنید. زمانی که قصد ساخت یک اپلیکیشن تک صفحه‌ای با کامپوننت‌های زیاد را داشته باشید این کتابخانه‌ها می‌توانند به شما کمک بزرگی بکنند. البته با وجود آنکه روند یادگیری کار با این کتابخانه‌ها ممکن است کمی سخت باشد اما در نهایت فواید منحصر به فردی دارند.
        Styled-JSX یک کتابخانه CSS-in-JS است که به شما قابلیت استفاده از JSX را می‌دهد. JSX یکی از راه‌هایی است که با آن می‌توانید کامپوننت‌های React را ایجاد کنید. شما می‌توانید این کتابخانه را هم در سمت کلاینت و هم در سمت سرور رندر کنید. برای استایل‌دهی به کامپوننت‌های JSX در این کتابخانه باید از Template Literalهای ES6 استفاده کنید.
        حجم کتابخانه در زمان اجرا تنها ۳ کیلوبایت است که این موضوع به لطف فشرده‌سازی زیاد و همچنین gzip اتفاق افتاده است.
