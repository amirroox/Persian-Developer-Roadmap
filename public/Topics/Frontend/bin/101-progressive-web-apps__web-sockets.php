        حتما تابحال شماهم از نسخه های تحت وب پیام رسان های معروف مانند تلگرام  ویا واتساپ استفاده کرده اید . آِیا تابحال به این نکته توجه داشتید که چرا سرعت ارسال و دریافت پیام (بدون در نظر گرفتن سرعت اینترنت شخصی) در این پیام رسان های تحت وب بالا است ، حال آنکه در برخی از پیام رسان های شخصی که برای برخی از شرکت ها طراحی میشوند ،سرعت ارسال و دریافت پیام پایین است . یکی از علت های این امر میتواند ، عدم استفاده از فناوری WebSocket باشد.
