    <h2>ری اکت</h2>
    <br>
    <p>
        ری اکت یکی از کتابخانه های اوپن سورس (متن باز) جاوا اسکریپت هست که برای ساخت رابط های کاربری وب سایت ها مورد
        استفاده قرار می گیرد.
        کتابخانه ری اکت توسط جامعه ای از توسعه دهندگان فیس بوک ارائه شد. ریکت ابتدا در سال 2011 با هدف استفاده توسط خود
        فیسبوک ایجاد شد اما بعد ها با توسعه بیشتر و تکمیل شدنش در سال 2013 به یک کتابخانه عمومی برای استفاده همه برنامه
        نویس ها تبدیل شد.
        ری اکت (React) به عنوان یکی از محبوب‌ترین کتابخانه‌های جاوا اسکریپتی و یکی از مهمترین ابزارهای توسعه وب شناخته
        می‌شود. با توجه به مزایایی که دارد، ری اکت برای توسعه دهندگان وب، یکی از انتخاب‌های برتر و بی‌رقیب بوده و به
        مرور، جایگاه خود را تثبیت کرده است.
        فرض کنید یکی از دوستان شما عکسی را در اینستاگرام پست کرده است. حالا شما اگر بروید و تصویر را لایک کنیدکرده و سپس
        نظرات را بررسی کنید، در حالی که نظرات را بررسی می‌کنید، می‌بینید که تعداد لایک ها 100 عدد از زمانی که شما پست را
        لایک کردید، افزایش یافته است، بدون اینکه شما صفحه را رفرش کنید. این دقیقاً همان کاری است که React انجام می‌دهد.
    </p>
