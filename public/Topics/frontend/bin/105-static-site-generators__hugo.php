    <h2>هیوگو</h2>
    <br>
    <p>
        هیوگو یک وبسایت فریم ورک عمومی است. مطابق اصول هیوگو یک استاتیک سایت جنریتور (static site generator ) است. بر
        خلاف سایر وب سایت ها(اغلب سایت ها) که به طور پویا زمانی که کاربر ریکوست (request) می دهد؛ صفحه را می سازند؛
        هیوگو عملیات ساخت را زمانی که خودتان محتوا را ایجاد کردید انجام می دهد. در حقیقت وقتی بازدیدهای یک سایت نسبت به
        تغییرات آن خیلی بیشتر باشد ؛ هیوگو کاربرد پیدا می کند. هیوگو برای سایت های نمایشی بهینه شده است. سایت های ساخته
        شده با هیوگو بسیار سریع و امن هستند.
    </p>
