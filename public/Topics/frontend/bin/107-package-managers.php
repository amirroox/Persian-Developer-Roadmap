    <h1>Package Managers</h1>
    <h2>مدیر بسته</h2>
    <br>
    <p>
        package manager یا بسته مجموعه ای از ابزار ها برای نصب و بروزرسانی و یا حتی حذف است.
        ما با استفاده از package manager تصمیم میگیریم که کدام نسخه کدام ورژن از هر بسته نصب باشد و هر بار که لازم بود
        پکیج ها آپدیت شوند.
        مهمترین مزیت این هست که دیگر نیازی نیست که درگیر وابستگی‌ها شویم و خود package manager قبل از نصب کتابخانه مورد
        نظر وابستگی‌ها را پیدا کرده و به پروژه اضافه می کند.
        امروزه با توجه به نوع زبان و تکنولوژی مورد استفاده package manager های مختلفی وجود دارد که هرکدام روش کار مخصوص
        به خود را دارند. برای مثال برای node js می‌توان npm و yarn را نام برد و یا برای زبان برنامه نویسی جاوا می‌توان
        Gradle ، maven را نام برد.
    </p>
