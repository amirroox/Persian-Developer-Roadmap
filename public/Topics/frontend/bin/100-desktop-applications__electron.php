    <h1>electron</h1>
    <h2>الکترون</h2>
    <br>
    <p>
        Electron یک فریم ورک متن باز است که توسط GitHub توسعه داده شده و نگهداری می شود. این فریم ورک برای ساخت برنامه های دسکتاپ پلت فرم متقابل با جاوا اسکریپت، HTML و CSS مورد استفاده قرار می گیرد. Electron اجازه می دهد تا توسعه برنامه های کاربردی GUI دسکتاپ با استفاده از اجزای برنامه نویسی جلو و عقب در ابتدا برای برنامه های کاربردی وب توسعه پیدا کند. از Node js برای برنامه نویسی Backend و از Chromium برای ساخت ظاهر استفاده می شود.
        اگر شما می توانید یک وب سایت را ایجاد کنید، با استفاده از این فریم ورک قادر خواهید بود برنامه دسکتاپ آن را نیز ایجاد کنید. Electron یک چارچوب برای ایجاد برنامه های بومی با تکنولوژی های وب مانند جاوا اسکریپت، HTML و CSS است. این فریم ورک از قطعات برنامه سخت مراقبت می کند تا بتوانید بر روی هسته برنامه خود متمرکز شوید.
    </p>
