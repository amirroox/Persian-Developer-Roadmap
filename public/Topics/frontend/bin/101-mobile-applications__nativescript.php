    <h1>NativeScript</h1>
    <h2>نیتیو اسکریپت</h2>
    <br>
    <p>
        NativeScript یک فریمورک متن‌باز است که با استفاده از آن با بهره‌گیری از تکنولوژی‌های مبتنی بر وب می‌توانید
        اپلیکیشن‌های محلی آندروید و آی‌او‌اس را ایجاد کنید. این بدان معناست که می‌توانیم اپلیکیشن‌های موبایل را با
        استفاده از جاوااسکریپت، تایپ‌اسکریپت و/یا انگولار ایجاد نمایید. مبنای این فریمورک براساس تفکر یک بار بنویسید و
        همه جا اجرا کنید است.
        اپلیکیشن‌های طراحی شده با استفاده از این فریمورک میزان نزدیکی بیشتری به اپلیکیشن‌های اصلی را نسبت به مواردی
        مانند PhoneGap ارائه می‌دهند. از آن‌جایی که این موارد اپلیکیشن‌های محلی هستند پس شما می‌توانید از تمام کارایی و
        نکات مثبت موجود در این فریمورک‌ها استفاده کنید.
    </p>
