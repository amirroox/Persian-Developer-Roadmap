    <h2>پُست سی اس اس</h2>
    <br>
    <p>
        به طور خلاصه postcss ابزاری است که با استفاده از جاوااسکریپت فایل های css ما را تغییر می دهد و این تغییر بسته به
        نیاز ما است و ما باید متناسب با نیازمان پلاگین متناسب را پیدا و یا ایجاد، و توسط ابزار postcss برروی فایل های
        css اعمال نماییم.
        پُست سی‌اس‌اس یک ابزار توسعه نرم‌افزار است که از افزایه‌های مبتنی بر جاوا اسکریپت برای خودکارسازی عملیات معمول
        سی‌اس‌اس استفاده می‌کند.
        برخلاف سَس و لَس، پست سی‌اس‌اس یک زبان الگوی کامپایل شده با سی‌اس‌اس نیست بلکه چارچوبی برای توسعه ابزارهای CSS
        است.
        (در واقع نمیشه گفت جزو preprocessors هست اما دسته بندی دیگه ایی هم نداره!)
    </p>
