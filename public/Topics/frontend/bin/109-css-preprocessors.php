    <h1>CSS Preprocessors</h1>
    <h2>پیش پردزانده های CSS</h2>
    <br>
    <p>
        پیش پردازنده (preprocessor) های CSS با هدف صرفه جویی در وقت و میزان کار یک توسعه دهنده وب، قابلیت هایی را به
        فایل های CSS می افزایند. اگر تجربه کدنویسی با هر زبان پیش پردازش CSS ای را داشته باشید، می توانید آن را به نوع
        داده دیگری تبدیل کنید.
        پیش پردازنده (preprocessor) ها می توانند با asset هایی مانند متغیرها، اپراتورها، interpolation ها، توابع و
        mixins، عملکرد CSS ها را ارتقاء دهند. همه asset های فوق برای افزایش کارایی و بهره وری با سینتکس معمولی CSS ادغام
        شده اند.
        هنگامی که تکامل وب را در طول چندین سال مورد مطالعه قرار می دهید، به راحتی می توانید مشاهده نمائید که استفاده از
        css با وضعیت فعلی کافی نیست. CSS تقریبا یک مفهوم ابتدایی است. و هنگامی که به پروژه های پیچیده برخورد می کنید،
        استفاده از CSS حتی سخت تر هم می شود. از سوی دیگر، مشخصه های جدید نیز بطور مداوم به HTML افزوده می شوند.
        شما می توانید از طریق تفکیک تعاریف به فایل های کوچک تر و انتقال آن ها به یک فایل بزرگ، CSS بهتری بنویسید. در
        حالی که این کار سبب می شود component ها با موفقیت ارائه شوند، می تواند منجر به ایجاد مشکلاتی مانند تکرار کد و
        مدیریت (maintenance) دشوار آن نیز بشود.
        راه حل های مختلفی برای حل این مشکل بررسی شده اند اما هیچ کدام از آن ها نتوانسته اند مشکل مدیریت و نگهداری
        (maintenance) کد ها را که هنوز هم پابرجاست، حل کنند. این جا همان جایی است که پیش پردازنده (preprocessor) های CSS
        وارد صحنه می شوند. این پیش پردازنده (preprocessor) ها با ویژگی های پیشرفته ای ادغام شده اند که نه تنها در نوشتن
        کدهای قابل استفاده مجدد به شما کمک می کنند، بلکه می توانید کدهای خود را به راحتی به کدهایی قابل انعطاف و قابل
        مدیریت و نگهداری (maintenance) تبدیل کنید. پیش پردازنده (preprocessor) ها باعث افزایش بهره وری سازندگان سایت
        میشوند.
    </p>
