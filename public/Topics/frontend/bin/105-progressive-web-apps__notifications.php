    <h2>Notifications API</h2>
    <br>
    <p>
        این API که زیرمجموعه ی Web API ها محسوب می شود، کاری می کند که بتوانیم در سطح سیستم(system level) اعلان نمایش
        دهیم. با استفاده از این API میتوانیم در سیستم عامل، یک system notification ایجاد کنیم. به عبارت دیگر اعلانی که
        از این طریق ایجاد می کنیم، خارج از صفحه (بیرون از viewport) و در سطح سیستم عامل (OS) است. برای همین اگر
        اپلیکیشنمان در حالت idle باشد، یا اینکه در background کار کند، باز هم می تواند این اعلان ها را به کاربر نشان
        دهد. و این همان مشکلی بود که اعلان های درون برنامه ای قادر به حل آن نبودند!
    </p>
