    <h1>Webpack</h1>
    <h2>وب پک</h2>
    <br>
    <p>
        WebPack یکی از بهترین و محبوب ترین کامپایلر ها و module bundler ها در دنیای وب می باشد.
        Webpack، کار باندل کردن همه‌ی فایل‌های مورد استفاده در پروژه شما را انجام خواهد داد. منظور از باندل کردن این هست که تمامی فایل‌های جاوااسکریپت پروژه شما در یک فایل قرار خواهد گرفت تا در هنگام لود شدن صفحات وب، فقط از یک فایل استفاده شود.
        علاوه بر آن قابلیت هایی مهم دیگری نیز داراست، مانند تبدیل فایل‌هایی که از preprocessor ها و superset ها استفاده میکنند (مثل sass و typescript) به کدهای قابل درک برای مرورگر (مثل js و css) و یا قابلیت مدیریت فایل‌های پروژه و سایر Asset ها به صورت اتوماتیک.
        برای شروع کار با Webpack احتیاج به هیچ پیش نیازی ندارید. کافی است آن را با استفاده از npm در پروژه ی خود نصب کنید و توسط node دستور webpack را اجرا کنید.
    </p>
