    <h1>Task Runners</h1>
    <h2>تسک رانر</h2>
    <br>
    <p>
        از آنجایی که توسعه Front-end پیچیده‌تر و سخت‌تر شده است، توسعه‌دهندگان باید راه‌هایی برای ساده‌سازی گردش‌های کاری خود و خودکارسازی کارهای تکراری بیابند. این منجر به ظهور Task Runner ها و Building tool ها شده است.
        Task Runners ها ابزارهایی هستند که کار های تکراری را در توسعه <b>خودکار</b> میکنند.
        این کارها ممکن است شامل کامپایل کد SASS یا LESS، بهینه سازی تصاویر، الحاق و کوچک کردن فایل های جاوا اسکریپت و CSS و موارد دیگر باشد. با خودکار کردن این وظایف، توسعه دهندگان می‌توانند در زمان صرفه جویی کرده و احتمال خطا را کاهش دهند.
    </p>
    <br>


