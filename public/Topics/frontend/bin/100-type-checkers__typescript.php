    <h1>TypeScript</h1>
    <h2>تایپ اسکریپت</h2>
    <br>
    <p>
        تایپ اسکریپت (به انگلیسی: TypeScript) یک زبان برنامه‌نویسی چندسکویی، متن باز و کامپایلری است که توسط شرکت
        مایکروسافت توسعه داده شده و پشتیبانی می‌شود. تایپ اسکریپت یکی از زیر مجموعه‌های زبان جاوا اسکریپت است که به شما
        کمک می‌کند تا کدهای خود را به روش ساده‌تری نوشته و سپس با کامپایل‌ کردن آن‌ها خروجی استاندارد کد جاوا اسکریپت را
        به دست آورید.
        امکانات پیشرفته‌ای که مایکروسافت به تایپ اسکریپت افزوده است، آن را به زبان‌هایی مانند #C، جاوا و… نزدیک ساخته
        است. تایپ اسکریپت برای توسعه‌ی پروژه‌های بزرگ جاوا اسکریپت طراحی شده است و فرآیند توسعه را تا حد زیادی ساده
        می‌کند.
    </p>
    <br>


