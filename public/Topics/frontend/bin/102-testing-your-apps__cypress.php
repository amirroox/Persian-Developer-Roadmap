    <p>
        Cypress، یک فریم ورک آزمایشی مبتنی بر جاوا اسکریپت است که بر روی Mocha ساخته شده است. یک فریم ورک آزمایشی جاوا
        اسکریپت غنی از ویژگی ها که در مرورگر اجرا می شود و آزمایش های ناهمزمان را ساده و راحت می کند. همچنین، از یک
        کتابخانه BDD/TDD و یک مرورگر برای جفت شدن با هر فریم ورک تست جاوا اسکریپت استفاده می کند.
        Cypress، منتظر است تا فرمان آزمایشی را به طور خودکار اجرا کرده قبل از اجرای دستورات بعدی که باید اجرا شود،
        بیانیه هایی را اعمال می کند.
        در حین اجرای مورد آزمایش، اسکرین شات می گیرد تا به اشکال زدایی کمک کند.
        اجرای تست را به طور موثرتری اشکال زدایی می کند زیرا اجرای تست تعاملی را با Test Runner و گزارش های مربوطه ارائه
        می دهد.
        به دلیل تفاوت طراحی معماری با سایر چارچوب های آزمایشی، اجرای سریع تر و قابل اطمینان تری را در اختیار کاربران
        قرار می دهد.
    </p>
