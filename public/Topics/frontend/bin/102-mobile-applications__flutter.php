        فلاتر یک فریم ورک متن باز برای ساخت اپلیکیشن‌های موبایل است. این کتابخانه توسط شرکت گوگل در سال 2017 معرفی شد و
        در مقایسه با React یا سایر فریم ورک‌های ساخت اپلیکیشن مبتنی بر موبایل عمر زیادی ندارد. فلاتر با زبان برنامه
        نویسی دارت نوشته شده است، یک زبان چندمنظوره و شی گراء که هنوز آنطور که باید و شاید بر سر زبان‌ها نیفتاده و
        محبوبیت زیادی ندارد. با یادگیری زبان دارت و فریم ورک فلاتر می‌توانید برای اندروید و iOS اپلیکیشن بسازید. جالب
        است بدانید علی بابا، یکی از بزرگترین شرکت‌های چینی در دنیا به فلاتر اعتماد کرده و اپلیکیشن خود را با این فریم
        ورک ساخته است.
        همچنین شرکت گوگل نسخه دوم فلاتر یا Flutter 2 را معرفی کرد. نسخه دوم این رابط کاربری متن باز قابلیت‌های جدیدی
        خواهد داشت. فلاتر که در ابتدا با تمرکز برنامه نویسی برای پلتفرم های موبایل کار را شروع کرد، در این نسخه جدید بال
        های خود را بازتر کرده و قرار است فضاهای بیشتری را پشتیبانی کند. Flutter حال می‌تواند در حوزه وب اپ ها و سیستم
        عامل های دسکتاپ نیز وارد رقابت شود. برنامه نویسان می‌توانند با یک بار نوشتن کد، برنامه خود را به عنوان اپلیکیشن
        IOS ، اپلیکیشن اندروید ، نرم‌افزار ویندوز ، نرم‌افزار لینوکس و نرم‌افزار مک تحویل بگیرند.
