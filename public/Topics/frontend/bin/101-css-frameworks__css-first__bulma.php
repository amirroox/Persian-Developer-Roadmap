    <p>
        فریمورک Bulma یکی از جدیدترین فریمورک های css میباشد که برای طراحی فرانت اند وب سایت ها کاربرد دارد. این فریمورک
        کوچک و سریع است.
        فریمورک Bulma یک فریمورک سبک و زیبا برای css است و چون برپایه ی موبایل طراحی شده است ابتدا هدفش موبایل است سپس
        تبلت و سپس کامپیوتر؛
        این یکی از مزیت های bulma است چون دیگر اکثر مخاطبین یک وب سایت کاربران دستگاه های کوچک مثل گوشی های هوشمند و
        تبلت ها هستند تا دسکتاپ !
        یکی دیگر از مزیت های bulma این است که به هیچ کد جاوااسکریپت یا فریمورک جاواسکریپتی نیاز ندارد . با این کار تمرکز
        طراح از روی کدهای سی اس اس به سمت جاوااسکریپت پرت نمیشود و تمرکزش روی طراحی قالب سی اس اس است و نتیجه ی زیباتر و
        قویتری در پی خواهد داشت .
    </p>
