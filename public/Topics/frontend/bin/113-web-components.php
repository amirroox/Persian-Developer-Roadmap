        ایزوله بسازیم تا در برنامه‌های دیگر قابل‌استفاده باشند. اگر از فریم‌ورک‌های Vue یا React یا Angular استفاده کرده
        باشید حتما با کامپوننت‌ها آشنا هستید. Web Component ها چنین قابلیتی را به حوزه‌ی وب می‌آورند بدون اینکه نیازی به
        فریم‌ورک خاصی باشد.
