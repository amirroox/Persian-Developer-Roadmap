    <h1>Module Bundlers</h1>
    <h2>باندلرهای ماژول</h2>
    <br>
    <p>
        منظور از باندل کردن این هست که تمامی فایل‌های جاوااسکریپت پروژه شما در یک فایل قرار خواهد گرفت تا در هنگام لود
        شدن صفحات وب، فقط از یک فایل استفاده شود. علاوه بر آن قابلیت هایی مهم دیگری نیز داراست، مانند تبدیل فایل‌هایی که
        از preprocessor ها و superset ها استفاده میکنند (مثل sass و typescript) به کدهای قابل درک برای مرورگر (مثل js و
        css) و یا قابلیت مدیریت فایل‌های پروژه و سایر Asset ها به صورت اتوماتیک.
        معمولاً با یک فایل ورودی شروع می شود و از آنجا همه کدهای مورد نیاز برای آن فایل ورودی را جمع می کند.
    </p>
