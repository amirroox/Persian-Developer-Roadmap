        کم‌تر و رسیدن به نتیجه مورد نظر در کوتاه‌ترین زمان، از فریم‌ورک‌های این زبان مانند Vue ، React، Angular و…
        استفاده می‌کنند.
        ویو یک فریم‌ورک متن باز زبان javascript است که از آن برای توسعه رابط کاربری استفاده می‌شود. Vue.js برای اولین
        بار در دسامبر سال 2013 توسط Evan You، یکی از کارمندان سابق گوگل، به دنیا معرفی شد. ایوان که در شغل پیشین خود از
        فریمورک Angular js در توسعه برنامه‌ها استفاده می‌کرد، تصمیم گرفت تا فریم‌ورکی برای استفاده در پروژه‌های خود خلق
        کند که به اندازه Angular قدرتمند و در عین حال سبک‌وزن و انعطاف‌پذیر باشد.
        شما می‌توانید از Vue.js در ساخت اپلیکیشن‌های تحت وب تک صفحه‌ای یا حتی وب‌سایت‌هایی در مقیاس بزرگ با ساختارهای
        مختلف استفاده کنید. همچنین می‌توانید کامپوننت‌هایی ایجاد کرده و در سایر بخش‌های پروژه یا حتی پروژه‌های دیگر از
        آن استفاده کنید.
        همچنین یکی از موراد پر استفاده از ویو جی اس در SSR است !
