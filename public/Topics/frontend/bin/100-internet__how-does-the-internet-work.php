    <h1>how does the internet Work?</h1>
    <h2>اینترنت چگونه کار میکند ؟</h2>
    <br>
    <p>
        خیلی از ماها از اینترنت زیاد استفاده می‌کنیم، اما شاید ندونیم که اینترنت دقیقا چیه و چطوری کار میکنه ؟
        اینترنت (Internet)، در اصل یک شبکه است که از اتصال کامپیوترهایی در سرتاسر جهان شکل گرفته برای نقل و انتقال
        اطلاعات و به عبارت Interconnected computer networks اشاره داره که به معنای شبکه‌های کامپوتری به هم پیوسته است.
        این کلیت ماجراس که کامپیوترهایی در سرتاسر جهان به هم وصل می‌شن و اطلاعات نقل و انتقال می‌کنند.
        اما تعداد زیادی سخت افزارهای فیزیکی (از کابل های در سطح شهر و یا کف اقیانوس گرفته تا ماهواره‌هایی که در فضا به
        دور زمین می‌چرخن) وجود داره که این ارتباط رو امکان‌پذیر میکنه.
        و همچنین تعدادی نرم‌افزار در پس زمینه کار وجود داره که به شما اجازه میده مثلا یک وبسایت رو سرچ کنید و درخواستی
        که دادین رو ببینید...
    </p>
    <br>


