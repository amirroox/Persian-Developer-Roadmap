        یکی از مهم ترین مشکلاتی که شرکت ها هنگام ساخت یک برنامه کاربردی یا اپلیکیشن موبایل با آن روبرو هستند، هزینه
        افزایشی ساخت یک اپلیکیشن بومی (Native) در سیستم عامل های مختلف است. در این میان، فریم ورکی به نام Ionic، یک
        انعطاف پذیری شگفت انگیز در ساخت اپلیکیشن های موبایل با استفاده از JavaScript و HTML5 ایجاد می کند.
        فریم ورک آیونیک (Ionic) یک کیت توسعه نرم افزار front-end (به اختصار SDK) است که در سال 2013 منتشر شده است. Ionic
        به شکل رایگان و متن باز (open-source) در اختیار کاربران قرار گرفته است و به آن ها این امکان را می دهد که برای
        برنامه نویسی اپلیکیشن های موبایل برای انواع سیستم عامل های iOS ،Windows Phone و Android، فقط از یک codebase
        استفاده کنند. در واقع می توان گفت که Ionic ابزاری چند سکویی (cross-platform) برای توسعه موبایل است.
