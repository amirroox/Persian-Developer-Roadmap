    <h2>فانوس دریایی گوگل</h2>
    <br>
    <p>
        Google lighthouse یا فانوس دریایی گوگل، یک ابزار کد باز (Open Source) و خودکار برای اندازه‌گیری کیفیت صفحات سایت
        است. Google Lighthouse با بررسی فاکتورهایی مانند قابلیت دسترسی، عملکرد و میزان بهینه بودن صفحات، شرایط لازم برای
        بهبود وب سایت شما را فراهم می‌کند.
        به بیان دیگر، فانوس دریایی یک اپلیکیشن است که برای وب سایت شما تعدادی audit (بازرسی) انجام می‌دهد. این بازرسی‌ها
        اطلاعات یک اپلیکیشن وب یا صفحه وب سایت را آنالیز می‌کند و سپس در مورد اینکه عملکرد یک اپلیکیشن یا یک صفحه وب تا
        چه اندازه خوب بوده، گزارشی ارائه می‌دهد.
        از آنجا که یکی از مهم‌ترین فاکتورهای رتبه بندی گوگل، مربوط به تجربه کاربری (UX) است، گوگل لایت هاوس، می‌تواند
        ابزاری کارآمد و مفید برای اطمینان از عملکرد وب سایت شما در این زمینه باشد.
    </p>
