    <h1>RAIL Model</h1>
    <h2>مدل RAIL</h2>
    <br>
    <p>
        همانطور که می دانید راه های بسیار مختلفی برای تست سرعت سایت و صفحات وب وجود دارد و مدل های مختلفی برای تحلیل و
        نگاه به مفهوم سرعت معرفی شده اند؛
        همه می دانیم که پایین آوردن سرعت بارگذاری صفحات وب بسیار مهم است و نقش اصلی را در کاهش 1bounce rate ایفا می کند
        اما این کار فقط نیمی از مشکل را حل می کند. قسمت اعظم زمان کاربران، صرف صبر کردن برای دریافت پاسخ input ها و...
        از سایت شما می شود. بنابراین اگر می خواهید کاربرانتان راضی بوده و سایت را ترک نکنند باید وب سایت شما دائما سریع
        باشد! این همان کاری است که مدل RAIL انجام می دهد؛ مدل RAIL تمام جوانب مفهوم سرعت را مورد بررسی قرار می دهد نه
        تنها سرعت سرور شما را!
        مدل RAIL یک مدل بهینه سازی عملکرد برای برنامه های کاربردی وب است که بر بهبود عملکرد درک شده یک صفحه وب تمرکز
        دارد. مخفف عبارت Response، Animation، Idle و Load است و چهار هدف اجرایی زیر را شامل می شود:
    </p>
    <br>
    <ul>
        <li>Response : مدت زمانی که طول می کشد تا صفحه وب به ورودی کاربر پاسخ دهد باید کمتر از 100 میلی ثانیه باشد.</li>
        <li>Animation : مدت زمان لازم برای اجرای یک انیمیشن باید کمتر از 10 میلی ثانیه باشد.</li>
        <li>Idle : صفحه وب باید از زمان بیکاری برای انجام کارهای غیر حیاتی استفاده کند.</li>
        <li>Load : مدت زمانی که طول می کشد تا صفحه وب به طور کامل بارگذاری شود باید کمتر از 1 ثانیه باشد.</li>
    </ul>
