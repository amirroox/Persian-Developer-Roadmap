    <h1>Accessibility</h1>
    <h2>مفهوم دسترسی پذیری</h2>
    <br>
    <p>
        دسترس‌پذیری به رویه قابل استفاده ساختن وب‌سایت‌ها برای بیشترین افراد ممکن گفته می‌شود. ما به طور سنتی وقتی به
        دسترس‌پذیری فکر می‌کنیم، افراد دارای ناتوانی‌ها را در نظر می‌گیریم، اما رویه دسترس‌پذیر ساختن سایت‌ها موجب
        بهره‌مندی گروه‌هایی از قبیل افرادی که از دستگاه‌های موبایل استفاده می‌کنند یا افرادی که اتصالات اینترنتی کندی
        دارند نیز می‌شود.
        همچنین دسترس‌پذیری را می‌توان به این معنی در نظر گرفت که با همه افراد به صورت یکسان برخورد کنیم و صرف‌نظر از
        توانایی‌ها یا شرایط افراد، فرصت‌های برابری برای همه خلق کنیم. دقیقاً همان طور که استثنا کردن یک نفر از ورود
        فیزیکی به ساختمان صرفاً به این خاطر که ویلچیر استفاده می‌کند نادرست است، استثنا کردن افراد از ورود به وب‌سایت‌ها
        نیز به دلیل نقص بینایی کار درستی محسوب نمی‌شود. همه ما تفاوت‌هایی داریم، اما در نهایت همه ما انسان هستیم و از
        این رو حقوق انسانی یکسانی داریم.
        به طور خلاصه دسترسی پذیری به معنی این است که وب سایت ها، ابزارها و فناوری ها به گونه ای طراحی و توسعه یافته اند
        که افراد دارای معلولیت بتوانند به راحتی از آنها استفاده کنند.
    </p>
