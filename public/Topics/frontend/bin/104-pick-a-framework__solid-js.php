    <h2>سالید جی اس</h2>
    <br>
    <p>
        سالید جی اس (Solid JS) مانند React JS یک کتابخانه Javascript برای ایجاد رابط کاربری در برنامه های وب است. از DOM
        مجازی استفاده نمی کند. مشابه Svelte ، دارای کامپایلری است که قالب ها را به گره های DOM واقعی کامپایل می کند و به
        روز رسانی ها را در واکنش های ریز پیچیده می کند. این باعث می شود یک برنامه وب به شدت بهینه سازی شده ، فوق العاده
        سبک و بسیار سریع داشته باشیم. به لطف رویکرد کامپایلر خود ، Solid می تواند به راحتی از ویژگیهای معروف React مانند
        Context ، Portals ، Fragments و موارد دیگر بدون قربانی کردن اندازه برنامه (کامپایل کردن فقط آنچه که شما استفاده
        می کنید) یا عملکرد پشتیبانی کند.
    </p>
