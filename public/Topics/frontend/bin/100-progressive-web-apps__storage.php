    <h2>Web Storage API</h2>
    <br>
    <p>
        با web storage، برنامه های کاربردی وب می توانند داده ها را به صورت محلی در مرورگر کاربر ذخیره کنند.
        قبل از HTML5، داده های برنامه باید در کوکی ها ذخیره می شد که در هر درخواست سرور گنجانده می شد. web storage ایمن
        تر است و مقادیر زیادی از داده ها را می توان به صورت محلی ذخیره کرد، بدون اینکه بر عملکرد وب سایت تأثیر بگذارد.
        برخلاف کوکی ها، محدودیت ذخیره‌سازی به مراتب بیشتر است (حداقل 5 مگابایت) و اطلاعات هرگز به سرور منتقل نمی شود.
    </p>
