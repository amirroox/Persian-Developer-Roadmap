    <h2>طراحی وب سایت ریسپانسیو</h2>
    <br>
    <p>
        گوگل طراحی ریسپانسیو(واکنش گرا) را برای طراحی وب سایت پیشنهاد میدهد.
        سایت های ریسپانسیو دارای url و html یکسان هستند و این قابلیت را دارند که روی تمامی دستگاه ها(با سایزهای مختلف
        صفحه نمایش) به درستی نمایش داده شوند و کار کنند.
        این موضوع باعث میشود که گوگل بتواند راحت تر و بهینه تر محتوای سایت رو نشانه گذاری کند.
        ولی وقتی که برای موبایل سایتی جداگانه طراحی و منتشر کنیم، url و html سایت مخصوص موبایل با سایت طراحی شده برای
        کامپبوتر متفاوت است. به همین دلیل گوگل برای نسخه های مختلف یک وب سایت چندین بار سازمان دهی و نشانه گذاری اطلاعات
        را انجام میدهد. این موضوع تأثیرات منفی در روند رتبه بندی گوگل دارد.
    </p>
