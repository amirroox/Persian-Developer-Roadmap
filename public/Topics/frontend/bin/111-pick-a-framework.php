    <p>
        به زبان ساده، فریم ورک برنامه نویسی ابزاری است که برای ساده‌تر کردن روند برنامه نویسی از آن استفاده می شود.
        در گذشته ساختن یک نرم افزار نیازمند تسلط کامل به یک زبان برنامه نویسی و استفاده از تمامی قابلیت های آن برای
        ایجاد امکانات بیشتر در برنامه بود.
        در آن زمان خیلی طبیعی بود که برنامه نویس ها به تمامی اشاره گرها (pointers)، توابع (functions) اهمیت زیادی بدهند،
        چون کیفیت کد آنها به این موارد وابسته بود. ولی در برنامه نویسی مدرن، همه این بخش ها به طور خودکار انجام می شوند
        تا سرعت کار بالا برود.
        فریمورک ها راه حل های از پیش نوشته شده و دسته بندی شده ای را ارائه می دهند که شما می توانید به کمک آنها ایرادات
        و خطا های معمول برنامه نویسی را به حداقل برسانید. چرا باید برای یک مشکلی که برایمان پیش آمده کد بنویسیم، در حالی
        که راه حل بهتر و مناسب آن قبلا نوشته شده و تنها کاری که ما باید انجام دهیم استفاده از آن است.
    </p>
