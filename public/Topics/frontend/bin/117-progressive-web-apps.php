    <h1>Progressive Web Apps</h1>
    <h2>برنامه وب پیشرو</h2>
    <br>
    <p>
        Progressive Web App یا همان PWA، برنامه‌هایی هستند که با استفاده از زبان‌های HTML، CSS و JavaScript توسعه داده
        شده‌اند اما این قابلیت را در اختیار ما قرار می‌دهند که برنامه‌های وب را با بهره بردن از APIهای مدرن مرورگر مانند
        یک برنامه‌ی Native بر روی تلفن‌های همراه نصب کنیم و فرقی ندارد کاربر شما از چه سیستم‌عاملی استفاده می‌کند.
        منظور ما از برنامه‌های Native، همان برنامه‌هایی هستند که به‌طور اختصاصی با یک زبان برنامه‌نویسی خاص مانند Java
        یا Swift برای سیستم‌عامل‌های Android یا iOS توسعه داده شده‌اند.
    </p>
