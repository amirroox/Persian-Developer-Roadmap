    <p>
        زمانی که صحبت از طراحی سایت می‌شود، اولین مفاهیمی که به ذهن ما می‌رسند HTML و CSS هستند. دو زبان نشانه گذاری که
        اولی اسکلت صفحات وب را ساخته و دومی مثل پوست و استخوان روی این بدن سوار می‌شود. هر چند HTML و CSS از شما متخصص
        برنامه نویس رابط کاربری نمی‌سازد، اما یادگیری آن‌ها برای حرفه‌ای شدن ضروری است. با اینکه CSS خالص (Pure CSS) به
        تنهایی بسیار قدرتمند است و تقریبا هر نوع تغییری در ظاهر سایت با آن امکان‌پذیر می‌باشد، اما گاهی به خاطر مسائلی
        مثل بالا بردن سرعت کار یا استاندارد کردن کدها سراغ فریم ورک‌های CSS می‌رویم.
        امروزه فریم ورک‌ها بسیار محبوب شده اند و استفاده از آنها تبدیل به یک استاندارد شده است. چون بسیاری از اجزا و
        اِلمان‌های طراحی سایت از قبل داخل کتابخانه‌ها تعریف شده اند و برنامه نویس می‌تواند به راحتی از آنها استفاده کند.
        به کمک فریم ورک دیگر لازم نیست چرخ را دوباره از نو بسازیم و درگیر مسائل تکراری بشویم.
    </p>
