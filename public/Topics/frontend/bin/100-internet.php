    <h1>Internet</h1>
    <h2>اینترنت</h2>
    <br>
    <p>
        در اولین گام برای یک فرانت اند کار نیاز است بدانید اینترنت چیست و چگونه کار میکند
        (البته در دوره زمونه الان همه بلدن)
        اما برای یک توسعه دهنده سمت کاربر نیاز است مفهوم کلیه اینترنت و کار کرد آن را بدانید.
        به طور کلی، اینترنت شبکه‌ای است که از اتصال کامپیوتر ها در سراسر جهان شکل گرفته است.
        دلیل اصلی به وجود آمدن این شبکه عظیم هم، تبادل اطلاعات بوده است.
        تعداد زیادی از دستگاه های کامپیوتری و الکترونیکی وجود دارند که این شبکه و برقراری ارتباط را ایجاد می کند.
        برخی از این تجهیزات در کف اقیانوس ها (مانند کابل ها) و برخی در آسمان (مانند ماهواره‌ها) وجود دارد.
    </p>
    <br>


