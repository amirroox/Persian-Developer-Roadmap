    <h2>بوت استرپ</h2>
    <br>
    <p>
        وب سایت رسمی بوت استرپ، آن را این گونه توصیف می کند : محبوب‌ ترین بستر HTML، CSS و JS برای توسعه پروژه ‌های واکنش ‌گرا و مبتنی بر موبایل در وب.
        به عبارت ساده تر : بوت استرپ مجموعه ای غول پیکر از بیت های مفید و قابل استفاده مجدد از کد است که به زبان HTML، CSS و جاوا اسکریپت نوشته شده اند. همچنین یک بستر توسعه فرانت اِند است که توسعه دهندگان و طراحان را قادر می سازد تا به سرعت وب سایت هایی کاملا واکنش گرا بسازند.
        اساسا بوت استرپ شما را از نوشتن کد های CSS مختلف نجات می دهد و باعث می شود که بتوانید زمان بیشتری را صرف طراحی صفحات وب خود کنید.
    </p>
