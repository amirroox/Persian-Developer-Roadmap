    <p>
        Yarn مخفف Yet Another Resource Negotiator می‌باشد که به عنوان یک مدیر بسته(package manager) برای جاوا اسکریپت
        است که بر سرعت، امنیت و ثبات تمرکز دارد. این کتابخانه متن باز بوده و توسط فیس‌بوک توسعه داده شده است و هدف از
        توسعه آن، سرعت بخشیدن به نصب پکیج‌ها و همچنین برای برطرف کردن حفره‌های امنیتی کتابخانه npm و موارد دیگر می‌باشد.
        Yarn در واقع تلاش فیسبوک برای جبران کاستی های npm بود! از نظر فنی Yarn به جای npm به وجود نیامده چرا که هنوز از
        ماژول های npm registry استفاده می کند، بلکه یک installer جدید است که هنوز از ساختار npm استفاده می کند. به زبان
        ساده تر رجیستری هنوز همان رجیستری است اما روش نصب متفاوت شده است. از آنجایی که Yarn و npm یک مجموعه پکیج را
        دارند اگر بخواهید از Yarn به npm یا بالعکس بروید نیازی به تغییرات سنگین نخواهید داشت.
    </p>
