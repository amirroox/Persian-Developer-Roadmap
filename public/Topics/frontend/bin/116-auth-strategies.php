    <h1>Authentication Strategies</h1>
    <h2>استراتژی احراز هویت</h2>
    <br>
    <p>
        استراتژی‌های احراز هویت روش‌ها یا تکنیک‌هایی هستند که برای تأیید هویت یک کاربر یا سیستم به منظور اعطای دسترسی به یک منبع محافظت شده استفاده می‌شوند. چندین استراتژی مختلف برای احراز هویت وجود دارد که می توان از آنها استفاده کرد، از جمله:
    </p>
    <br>
    <ul style="text-align: right">
        <li>Basic Authentication (احراز هویت پایه)</li>
        <li>Session Based Authentication (احراز هویت مبتنی بر سشن)</li>
        <li>Token Based Authentication (احراز هویت مبتنی بر توکن)</li>
        <li>JWT Authentication (احراز هویت مبتنی بر توکن JWT)</li>
        <li>OAuth</li>
        <li>SSO</li>
    </ul>
    <br>
    <p>
        لزومی نیست همه اینها را یاد بگیرد اما نحوه پیاده‌سازی کلی و آگاهی از کار آنها میتواند به شما بسیار کمک کننده باشد.
    </p>
