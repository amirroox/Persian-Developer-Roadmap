    <h1>Static Site Generators</h1>
    <h2>سایت ساز استاتیک (ایستا)</h2>
    <br>
    <p>
        Static Site Generator یا سایت‌سازهای ایستا به ابزار و سیستم‌های مدیریت و تولید سایت‌های ایستا (ثابت) گفته
        می‌شود. توسعه‌دهندگان و افرادی که به کار با خط فرمان علاقه داشتهاند، همواره به این ابزار نگاه مثبتی داشته‌اند.
        یک سایت‌ساز ایستا، فایل‌های سورس را می‌گیرد و یک وبسایت کاملا ایستا ایجاد می‌کند.
        سایت‌سازهای ایستای زیادی وجود دارند که برای اهداف مختلفی مورداستفاده قرار می‌گیرند. برخی از آنها برای ایجاد
        وبسایت‌های گالری عکس است که تصاویر را از فولدرهای عکس می‌گیرند و برخی دیگر به شما در ساخت وبسایت‌های اسناد کمک
        می‌کنند. اکثر آنها کمک می‌کنند که یک وبسایت عادی با یک وبلاگ را مدیریت کنید.
    </p>
