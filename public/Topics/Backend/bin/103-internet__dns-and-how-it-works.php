    <h1>DNS</h1>
    <h2>دی ان اس</h2>
    <br>
    <p>
        DNS، مخفف Domain Name System، یکی از پایه‌های اینترنت است و بیشتر ما در طول روز بدون آن‌که بدانیم از DNS استفاده
        می‌کنیم.
        DNS مانند یک دفترچه تلفن برای اینترنت است. همانطور که شما برای تماس با دیگران به جای بخاطر سپردن شماره‌ی آن‌ها،
        از دفترچه تلفن استفاده می‌کنید، DNS نیز مانند یک دفترچه تلفن عمل می‌کند و نیازی به حفظ کردن آدرس IP ها نیست.
        همانطور که می‌دانید، کامپیوترها برای اتصال به یکدیگر از اعداد یا همان IP آدرس‌ها استفاده می‌کنند.
        Domain Name System فهرست توزیع شده‌ای است که نام دامنه قابل خواندن توسط انسان مانند www.road-map.ir را به اعداد
        خوانا برای کامپیوترها یعنی IP آدرس تبدیل می‌کند. برعکس این نیز در مورد DNS صدق می‌کند، یعنی DNS سیستمی است که
        نام دامنه وب را سازماندهی می‌کند و آن‌ها را برای همه کسانی که می‌خواهند به شبکه وصل شوند، قابل‌فهم‌تر می‌کند.
        منظور از dns سرور چیست؟ بایستی گفت جهت اینکه یک دامنه بتواند به وب سایت اشاره نماید در قدم اول بایستی در DNS
        Server اضافه شود. اما کار dns server چیست؟ DNS سرور یک دیتابیس بزرگ است که شامل مجموعه ای از دامنه‌ها و IP های
        مرتبط می‌باشد. به عنوان مثال اگر دامنه google.com باشد سایت به IP با آدرس DNS 64.233.167.99 resolve می شود.
    </p>
