    <h2>مرورگر چیست و چگونه کار میکند؟</h2>
    <br>
    <p>
        مرورگر، اسمی است که به نرم افزارهایی اطلاق می‌شود که می‌توانید با آن‌ها وارد اینترنت شوید و از محتوایی که روی وب
        سایت های مختلف وجود دارد استفاده کنید. در واقع مرورگرها اطلاعات را از منابع مختلفی که در سرتاسر دنیا وجود دارد
        دریافت می‌کند و به شما اجازه می‌دهد به این دیتا دسترسی داشته باشید.
        مرورگر، یک برنامه نرم افزاری است که برای مکان یابی، بازیابی و نمایش محتوا در شبکه جهانی وب، از جمله صفحات وب،
        تصاویر، ویدئو و سایر فایل‌ها استفاده می‌شود. به عنوان یک مدل کلاینت/سرور، مرورگر کلاینتی است که روی رایانه‌ای
        اجرا می‌شود که با وب سرور تماس می‌گیرد و اطلاعاتی را درخواست می‌کند. وب سرور اطلاعات را به مرورگر وب می‌فرستد که
        نتایج را روی رایانه یا سایر دستگاه‌های فعال اینترنت که از مرورگر پشتیبانی می‌کند نمایش می‌دهد.
    </p>
